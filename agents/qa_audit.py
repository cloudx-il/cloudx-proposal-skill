SCORING_PROMPT = """You are a Salesforce Apex code reviewer for CLOUDX, an Israeli Salesforce consulting company.

Score this Apex class against the CLOUDX 150-point Apex Quality Rubric:

| Category | Max Points | What to Check |
|---|---|---|
| Governor Limits | 25 | No SOQL/DML in loops, bulkified triggers, collection-based operations |
| Security | 25 | WITH SECURITY_ENFORCED or stripInaccessible, no SOQL injection, no hardcoded IDs |
| Testing | 25 | Test class exists, coverage potential, bulk test patterns, no seeAllData |
| Error Handling | 20 | Try-catch on DML/callouts, meaningful messages, no empty catches, logging |
| Code Quality | 20 | Single Responsibility, method <40 lines, meaningful names, no magic numbers |
| Performance | 20 | Selective SOQL, Maps for lookups, async for heavy processing |
| Maintainability | 15 | Handler pattern, separation of concerns, Custom Metadata for config |

Respond ONLY with valid JSON (no markdown, no backticks):
{
    "scores": {
        "governor_limits": { "score": 0, "max": 25, "findings": ["..."] },
        "security": { "score": 0, "max": 25, "findings": ["..."] },
        "testing": { "score": 0, "max": 25, "findings": ["..."] },
        "error_handling": { "score": 0, "max": 20, "findings": ["..."] },
        "code_quality": { "score": 0, "max": 20, "findings": ["..."] },
        "performance": { "score": 0, "max": 20, "findings": ["..."] },
        "maintainability": { "score": 0, "max": 15, "findings": ["..."] }
    },
    "total_score": 0,
    "grade": "Excellent|Good|Fair|Problematic|Critical",
    "top_issues": ["issue 1", "issue 2", "issue 3"],
    "suggestions": ["suggestion 1", "suggestion 2"]
}"""


def analyze_with_ai(client, file_path: Path, content: str, model: str) -> dict | None:
    """Send file to Claude for deep analysis and scoring."""
    try:
        response = client.messages.create(
            model=model,
            max_tokens=2048,
            messages=[
                {
                    "role": "user",
                    "content": f"{SCORING_PROMPT}\n\n"
                    f"## File: {file_path.name}\n\n```apex\n{content}\n```",
                }
            ],
        )
        text = response.content[0].text.strip()
        # Try to parse JSON from response
        return json.loads(text)
    except json.JSONDecodeError:
        # Try to extract JSON from markdown code block
        match = re.search(r"```(?:json)?\s*(\{.*?\})\s*```", text, re.DOTALL)
        if match:
            try:
                return json.loads(match.group(1))
            except json.JSONDecodeError:
                pass
        return None
    except Exception as e:
        print(f"  ⚠ AI analysis failed for {file_path.name}: {e}", file=sys.stderr)
        return None


