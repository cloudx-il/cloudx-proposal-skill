def main():
    parser = argparse.ArgumentParser(
        description="CLOUDX QA Audit Agent — Scan Apex code against CLOUDX standards"
    )
    parser.add_argument(
        "--project", required=True, help="Path to Salesforce project root"
    )
    parser.add_argument(
        "--rules", default=None, help="Path to qa_rules.yaml (default: auto-detect)"
    )
    parser.add_argument(
        "--output", default=None, help="Output file path (default: stdout)"
    )
    parser.add_argument(
        "--quick-only",
        action="store_true",
        help="Skip AI analysis, run regex checks only (free)",
    )
    args = parser.parse_args()

    # Load config
    rules = load_rules(args.rules)
    scoring_config = rules.get("scoring", {})
    model = scoring_config.get("model", "claude-haiku-4-5")
    exclude = set(rules.get("exclude_files", []))

    # Find Apex files
    apex_files = find_apex_files(args.project)
    if not apex_files:
        print("No Apex files found in", args.project, file=sys.stderr)
        sys.exit(1)

    project_name = Path(args.project).resolve().name
    print(f"=== CLOUDX QA Audit: {project_name} ===", file=sys.stderr)
    print(f"Found {len(apex_files)} Apex files", file=sys.stderr)

    # Phase 1: Quick checks
    print("\n[Phase 1] Running quick checks...", file=sys.stderr)
    quick_results = {}
    all_files = apex_files  # For test class lookup
    for f in apex_files:
        if f.name in exclude:
            continue
        content = f.read_text(encoding="utf-8", errors="replace")
        quick_results[str(f)] = run_quick_checks(f, content, rules, all_files)
        status = "✅" if all(
            v.get("pass", True) for v in quick_results[str(f)].values()
        ) else "⚠"
        print(f"  {status} {f.name}", file=sys.stderr)

    # Phase 2: AI analysis
    ai_results = {}
    if not args.quick_only:
        api_key = os.environ.get("ANTHROPIC_API_KEY")
        if not api_key:
            print(
                "\n⚠ ANTHROPIC_API_KEY not set — skipping AI analysis.",
                file=sys.stderr,
            )
            print("  Set it to enable deep scoring with Claude.", file=sys.stderr)
        else:
            import anthropic

            client = anthropic.Anthropic()
            print(f"\n[Phase 2] AI analysis with {model}...", file=sys.stderr)
            for f in apex_files:
                if f.name in exclude:
                    continue
                content = f.read_text(encoding="utf-8", errors="replace")
                # Skip test classes and generated classes per config
                if scoring_config.get("skip_test_classes") and is_test_class(
                    f, content
                ):
                    continue
                if scoring_config.get("skip_generated") and is_generated_class(content):
                    continue
                print(f"  Analyzing {f.name}...", file=sys.stderr)
                result = analyze_with_ai(client, f, content, model)
                if result:
                    ai_results[str(f)] = result

    # Phase 3: Generate report
    print("\n[Phase 3] Generating report...", file=sys.stderr)
    report = generate_report(quick_results, ai_results, project_name)

    if args.output:
        Path(args.output).write_text(report, encoding="utf-8")
        print(f"\n✅ Report saved to {args.output}", file=sys.stderr)
    else:
        sys.stdout.buffer.write(report.encode("utf-8"))
        sys.stdout.buffer.write(b"\n")


if __name__ == "__main__":
    main()
