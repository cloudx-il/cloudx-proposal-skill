#!/usr/bin/env python3
"""
CLOUDX QA Audit Agent
Scans Apex classes against CLOUDX standards and scores them using Claude AI.

Usage:
    python qa_audit.py --project C:\\Projects\\DELL-Foundation
    python qa_audit.py --project . --rules qa_rules.yaml --output report.md
"""

import argparse
import json
import os
import re
import sys
from pathlib import Path

import yaml

# ---------------------------------------------------------------------------
# Phase 1: Quick regex-based checks (free, no API cost)
# ---------------------------------------------------------------------------

# Salesforce ID pattern: 15 or 18 alphanumeric chars that look like an ID
SF_ID_PATTERN = re.compile(r"""['"]([a-zA-Z0-9]{15}|[a-zA-Z0-9]{18})['"]""")
# Known prefixes that indicate a real SF ID (common object key prefixes)
SF_ID_PREFIXES = {
    "001", "003", "005", "006", "00D", "00e", "00G", "00N", "00Q", "00T",
    "00U", "012", "01I", "01p", "01Q", "01s", "01t", "02i", "02s", "03d",
    "04t", "0SO",
}

SOQL_KEYWORDS = re.compile(
    r"\b(SELECT\s+.+?\s+FROM\s+\w+)", re.IGNORECASE | re.DOTALL
)
DML_KEYWORDS = re.compile(
    r"\b(insert|update|delete|upsert|undelete|merge)\s+", re.IGNORECASE
)
LOOP_PATTERN = re.compile(r"\b(for\s*\(|while\s*\()", re.IGNORECASE)


def load_rules(rules_path: str | None) -> dict:
    """Load QA rules from YAML file or return defaults."""
    if rules_path and Path(rules_path).exists():
        with open(rules_path, encoding="utf-8") as f:
            return yaml.safe_load(f)
    # Check for qa_rules.yaml next to this script
    default_path = Path(__file__).parent / "qa_rules.yaml"
    if default_path.exists():
        with open(default_path, encoding="utf-8") as f:
            return yaml.safe_load(f)
    # Fallback: all rules enabled
    return {
        "rules": {
            "with_sharing": {"enabled": True, "severity": "critical"},
            "soql_limit": {"enabled": True, "severity": "warning"},
            "no_soql_in_loops": {"enabled": True, "severity": "critical"},
            "no_hardcoded_ids": {"enabled": True, "severity": "critical"},
            "recursion_guards": {"enabled": True, "severity": "warning"},
            "test_class_exists": {"enabled": True, "severity": "critical"},
            "try_catch_callouts": {"enabled": True, "severity": "warning"},
            "no_magic_numbers": {"enabled": True, "severity": "info"},
            "database_partial_dml": {"enabled": True, "severity": "info"},
            "aura_enabled_cacheable": {"enabled": True, "severity": "info"},
        },
        "scoring": {
            "model": "claude-haiku-4-5",
            "skip_test_classes": True,
            "skip_generated": True,
        },
        "exclude_files": [],
    }


def find_apex_files(project_path: str) -> list[Path]:
    """Find all .cls files in the project."""
    root = Path(project_path)
    cls_files = sorted(root.rglob("force-app/**/*.cls"))
    # Exclude -meta.xml companion files
    return [f for f in cls_files if not f.name.endswith("-meta.xml")]


def is_test_class(file_path: Path, content: str) -> bool:
    """Check if a file is a test class."""
    name = file_path.stem
    if name.endswith("_Test") or name.endswith("Test"):
        return True
    if "@isTest" in content or "@IsTest" in content:
        return True
    return False


def is_generated_class(content: str) -> bool:
    """Check if a class appears auto-generated."""
    indicators = ["// Generated by", "auto-generated", "DO NOT EDIT"]
    return any(ind.lower() in content.lower() for ind in indicators)


def rule_enabled(rules: dict, rule_name: str) -> bool:
    """Check if a specific rule is enabled."""
    return rules.get("rules", {}).get(rule_name, {}).get("enabled", True)


def check_with_sharing(content: str) -> dict:
    """Check if class uses 'with sharing'."""
    # Match class declaration
    class_decl = re.search(
        r"(public|global|private)\s+(virtual\s+|abstract\s+)?"
        r"(with sharing|without sharing|inherited sharing)?\s*class\s+",
        content,
    )
    if not class_decl:
        return {"pass": True, "detail": "No class declaration found (interface/enum?)"}
    sharing = class_decl.group(3)
    if sharing and "with sharing" in sharing:
        return {"pass": True, "detail": "Uses 'with sharing'"}
    if sharing and "without sharing" in sharing:
        return {"pass": False, "detail": "Uses 'without sharing' — needs justification", "severity": "critical"}
    if sharing and "inherited sharing" in sharing:
        return {"pass": True, "detail": "Uses 'inherited sharing'"}
    return {"pass": False, "detail": "No sharing keyword specified", "severity": "critical"}


def check_soql_limit(content: str) -> dict:
    """Check if SOQL queries include LIMIT."""
    queries = SOQL_KEYWORDS.findall(content)
    if not queries:
        return {"pass": True, "detail": "No SOQL queries found"}
    missing_limit = []
    for i, q in enumerate(queries, 1):
        if "LIMIT" not in q.upper() and "limit" not in q:
            missing_limit.append(f"Query #{i}")
    if missing_limit:
        return {
            "pass": False,
            "detail": f"SOQL without LIMIT: {', '.join(missing_limit)}",
            "severity": "warning",
        }
    return {"pass": True, "detail": f"All {len(queries)} queries have LIMIT"}


def check_soql_in_loops(content: str) -> dict:
    """Check for SOQL/DML inside loops (basic heuristic)."""
    lines = content.split("\n")
    loop_depth = 0
    issues = []
    for i, line in enumerate(lines, 1):
        stripped = line.strip()
        # Track loop depth (simplified)
        if LOOP_PATTERN.search(stripped):
            loop_depth += 1
        if loop_depth > 0:
            if re.search(r"\bDatabase\.\w+\(", stripped) or DML_KEYWORDS.search(stripped):
                issues.append(f"Line {i}: DML in loop")
            if re.search(r"\[.*SELECT.*FROM", stripped, re.IGNORECASE):
                issues.append(f"Line {i}: SOQL in loop")
        # Approximate loop end by counting braces
        open_braces = stripped.count("{")
        close_braces = stripped.count("}")
        loop_depth = max(0, loop_depth + open_braces - close_braces)
    if issues:
        return {"pass": False, "detail": "; ".join(issues[:5]), "severity": "critical"}
    return {"pass": True, "detail": "No SOQL/DML in loops detected"}


def check_hardcoded_ids(content: str) -> dict:
    """Check for hardcoded Salesforce IDs."""
    matches = SF_ID_PATTERN.findall(content)
    suspicious = []
    for m in matches:
        prefix = m[:3]
        if prefix in SF_ID_PREFIXES:
            suspicious.append(m[:8] + "...")
    if suspicious:
        return {
            "pass": False,
            "detail": f"Possible hardcoded IDs: {', '.join(suspicious[:3])}",
            "severity": "critical",
        }
    return {"pass": True, "detail": "No hardcoded IDs detected"}


def check_recursion_guards(content: str, file_path: Path) -> dict:
    """Check if handler classes have recursion prevention."""
    name = file_path.stem.lower()
    is_handler = "handler" in name or "trigger" in name
    if not is_handler:
        return {"pass": True, "detail": "Not a handler class — skipped"}
    if "isRunning" in content or "isExecuting" in content or "alreadyRun" in content:
        return {"pass": True, "detail": "Recursion guard found"}
    return {
        "pass": False,
        "detail": "Handler without recursion guard (isRunning pattern)",
        "severity": "warning",
    }


def check_test_class_exists(
    file_path: Path, all_files: list[Path], content: str
) -> dict:
    """Check if a corresponding test class exists."""
    if is_test_class(file_path, content):
        return {"pass": True, "detail": "This IS a test class"}
    name = file_path.stem
    possible_test_names = {f"{name}_Test", f"{name}Test"}
    all_names = {f.stem for f in all_files}
    found = possible_test_names & all_names
    if found:
        return {"pass": True, "detail": f"Test class: {found.pop()}.cls"}
    return {
        "pass": False,
        "detail": f"No test class found (expected {name}_Test.cls)",
        "severity": "critical",
    }


def check_try_catch_callouts(content: str) -> dict:
    """Check if HTTP callouts are wrapped in try-catch."""
    has_callout = bool(
        re.search(r"(HttpRequest|Http\.send|callout:)", content, re.IGNORECASE)
    )
    if not has_callout:
        return {"pass": True, "detail": "No callouts found"}
    has_try_catch = "try" in content and "catch" in content
    if has_try_catch:
        return {"pass": True, "detail": "Callouts wrapped in try-catch"}
    return {
        "pass": False,
        "detail": "HTTP callout without try-catch",
        "severity": "warning",
    }


def run_quick_checks(
    file_path: Path, content: str, rules: dict, all_files: list[Path]
) -> dict:
    """Run all regex-based quick checks on a single file."""
    results = {}
    is_test = is_test_class(file_path, content)
    exempt = {"pass": True, "detail": "Test class — exempt"}

    checks = {
        "with_sharing": lambda: exempt if is_test else check_with_sharing(content),
        "soql_limit": lambda: exempt if is_test else check_soql_limit(content),
        "no_soql_in_loops": lambda: exempt if is_test else check_soql_in_loops(content),
        "no_hardcoded_ids": lambda: check_hardcoded_ids(content),
        "recursion_guards": lambda: exempt if is_test else check_recursion_guards(content, file_path),
        "test_class_exists": lambda: check_test_class_exists(
            file_path, all_files, content
        ),
        "try_catch_callouts": lambda: exempt if is_test else check_try_catch_callouts(content),
    }
    for rule_name, check_fn in checks.items():
        if rule_enabled(rules, rule_name):
            results[rule_name] = check_fn()
    return results


