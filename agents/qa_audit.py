GRADE_THRESHOLDS = [
    (130, "Excellent"),
    (105, "Good"),
    (80, "Fair"),
    (55, "Problematic"),
    (0, "Critical"),
]


def score_to_grade(score: int) -> str:
    for threshold, grade in GRADE_THRESHOLDS:
        if score >= threshold:
            return grade
    return "Critical"


def severity_icon(severity: str) -> str:
    return {"critical": "🔴", "warning": "🟡", "info": "🔵"}.get(severity, "⚪")


def generate_report(
    quick_results: dict,
    ai_results: dict,
    project_name: str,
) -> str:
    """Generate a Markdown report from scan results."""
    lines = []
    lines.append(f"# QA Audit Report — {project_name}")
    lines.append("")
    lines.append(f"**Generated by**: CLOUDX QA Audit Agent")
    lines.append(f"**Files scanned**: {len(quick_results)}")
    lines.append("")

    # --- Summary table ---
    lines.append("## Summary")
    lines.append("")
    lines.append("| File | Quick Check | AI Score | Grade |")
    lines.append("|------|-----------|----------|-------|")

    total_score = 0
    scored_count = 0
    grade_counts = {"Excellent": 0, "Good": 0, "Fair": 0, "Problematic": 0, "Critical": 0}
    all_issues = []

    for file_path, checks in sorted(quick_results.items()):
        name = Path(file_path).stem
        fails = sum(1 for c in checks.values() if not c.get("pass", True))
        total_checks = len(checks)
        quick_status = f"{total_checks - fails}/{total_checks} passed"

        ai = ai_results.get(file_path)
        if ai:
            ai_score = ai.get("total_score", 0)
            grade = ai.get("grade", score_to_grade(ai_score))
            total_score += ai_score
            scored_count += 1
            grade_counts[grade] = grade_counts.get(grade, 0) + 1
            all_issues.extend(ai.get("top_issues", []))
            lines.append(f"| {name} | {quick_status} | {ai_score}/150 | {grade} |")
        else:
            lines.append(f"| {name} | {quick_status} | — | — |")

    avg_score = total_score / scored_count if scored_count else 0
    avg_grade = score_to_grade(int(avg_score))

    lines.append("")
    if scored_count > 0:
        lines.append(f"**Average score**: {avg_score:.0f}/150 ({avg_grade})")
        lines.append("")
        dist = " | ".join(f"{g}: {c}" for g, c in grade_counts.items() if c > 0)
        lines.append(f"**Grade distribution**: {dist}")
        lines.append("")

    # --- Top recurring issues ---
    if all_issues:
        # Count issue frequency
        issue_freq = {}
        for issue in all_issues:
            issue_freq[issue] = issue_freq.get(issue, 0) + 1
        top_issues = sorted(issue_freq.items(), key=lambda x: -x[1])[:5]
        lines.append("## Top Recurring Issues")
        lines.append("")
        for issue, count in top_issues:
            lines.append(f"- {issue} ({count}x)")
        lines.append("")

    # --- Per-file details ---
    lines.append("## Details per File")
    lines.append("")

    for file_path, checks in sorted(quick_results.items()):
        name = Path(file_path).stem
        lines.append(f"### {name}")
        lines.append("")

        # Quick checks
        lines.append("**Quick Checks:**")
        lines.append("")
        for rule_name, result in checks.items():
            icon = "✅" if result.get("pass") else severity_icon(
                result.get("severity", "info")
            )
            lines.append(f"- {icon} **{rule_name}**: {result.get('detail', '')}")
        lines.append("")

        # AI analysis
        ai = ai_results.get(file_path)
        if ai and "scores" in ai:
            lines.append("**AI Scoring (150 pts):**")
            lines.append("")
            lines.append("| Category | Score | Findings |")
            lines.append("|----------|-------|----------|")
            for cat, data in ai["scores"].items():
                score = data.get("score", 0)
                max_pts = data.get("max", 0)
                findings = "; ".join(data.get("findings", [])[:2])
                lines.append(
                    f"| {cat.replace('_', ' ').title()} | {score}/{max_pts} | {findings} |"
                )
            lines.append("")
            if ai.get("suggestions"):
                lines.append("**Suggestions:**")
                for s in ai["suggestions"][:3]:
                    lines.append(f"- {s}")
                lines.append("")

        lines.append("---")
        lines.append("")

    return "\n".join(lines)


